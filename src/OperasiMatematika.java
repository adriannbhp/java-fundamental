public class OperasiMatematika {
    public static void main(String[] args) {
        int a = 100;
        int b = 10;

        System.out.println(a + b); // Output : 110
        System.out.println(a - b); // Output : 90
        System.out.println(a * b); // Output : 1000
        System.out.println(a / b); // Output : 10
        System.out.println(a % b); // Output : 0

        int c = 100;

        c += 10;
        System.out.println(c); // Output : 110

        c -= 10;
        System.out.println(c); // Output : 100

        c *= 10;
        System.out.println(c); // Output : 100

        int d = 100;

        d++;
        System.out.println(d); // Output : 101

        d--;
        System.out.println(d); // Output : 100

    }
}
