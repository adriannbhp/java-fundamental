public class TipeDataChar {
    public static void main(String[] args) {

        /* Tipe Data Character
           - Data Character (huruf) di Java direpresentasikan oleh tipe char.
           - Untuk membuat data char di Java, kita bisa menggunakan tanda
             '(petik satu) di awal dan di akhir karakter
        */

        char e = 'E';
        char k = 'K';
        char o = 'O';

        System.out.print(e);
        System.out.print(k);
        System.out.print(o);
    }
}
